package RdyToUseObjects;

import java.awt.*;

public class Rectangle {
    private int a;
    private int b;
    private int c;
    private int d;
    private Color color;

    public Rectangle(int a, int b, int c, int d, Color color) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
        this.color = color;
    }

}
